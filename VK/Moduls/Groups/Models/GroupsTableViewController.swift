        guard let cell = tableView.dequeueReusableCell(withIdentifier: groupsViewControllerIdentifier, for: indexPath) as? TableViewCell
        else { return UITableViewCell() }
        
        cell.configureGroups(with: groups[indexPath.row])
        
        return cell
    }
